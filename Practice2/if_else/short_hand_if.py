# Short-hand If Examples (с разными знаками и текстом)

# Example 1
a = 52
b = 67
if a < b: print("b is greater than a")  # True b больше a

# Example 2
a = 100
b = 50
if a >= b: print("a is greater than or equal to b")  # True a больше или равно b

# Example 3
a = -10
b = -20
if a != b: print("a and b are not equal")  # True a не равно b

# Example 4
a = 0
b = 5
if a <= b: print("a is less than or equal to b")  # True a меньше или равно b

# Example 5
a = 33
b = 33
if a == b: print("a and b are equal")  # True a равно b


