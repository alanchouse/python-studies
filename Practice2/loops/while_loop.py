# Example 1
i = 2
while i < 12:
    print(i)
    i += 3  # Выведет 2, 5, 8, 11

# Example 2
num = 10
while num > 5:
    print(num)
    num -= 2  # Выведет 10, 8, 6

# Example 3
count = 0
while count < 10:
    print(count)
    count += 3  # Выведет 0, 3, 6, 9

# Example 4
x = 5
while x <= 15:
    print(x)
    x += 5  # Выведет 5, 10, 15

